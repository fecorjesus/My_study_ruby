#Retorne o próximo número do número inteiro passado

=begin
def aumenta_em_um(numero)
    resultado = numero + 1
    return resultado
end
  
# Exemplo de uso
numero_original = 6
novo_numero = aumenta_em_um(numero_original)
puts "Número original: #{numero_original}"
puts "Novo número: #{novo_numero}"
=end

#Converter idade em dias
=begin
def idade_em_dias(idade_em_anos)
    dias_por_ano = 365
    idade_em_dias = idade_em_anos * dias_por_ano
    return idade_em_dias
  end
  
  # Exemplo de uso
  idade_em_anos = 46
  idade_em_dias_resultado = idade_em_dias(idade_em_anos)
  puts "Idade echam anos: #{idade_em_anos}"
  puts "Idade em dias: #{idade_em_dias_resultado}"
=end

